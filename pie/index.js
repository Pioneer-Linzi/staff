export default class Pie
{
	constructor ( Ressive, Proxy, Promise, Princess, Self )
	{
		Self = Self || this

		Princess = new Promise(

			( resolve, reject ) => {

				new Ressive( Proxy )

					.next(

						({ Vue, Bus, Router, Vuex, I18n }) => {

							Vue.use( Bus ), Vue.use( Router ), Vue.use( Vuex ), Vue.use( I18n )

						},

						( Proxy ) => Self.Proxy = Proxy

					)

					.next(

						( /* Anita Mui */ ) => { /* 1963.10.10 - 2003.12.30 */ }

					)

				try
				{
					// Proxy Object 2 Project
					resolve( Proxy )
				}

				catch ( error )
				{
					reject( error )
				}

			}

		)

		// Finally Executer
		Princess.next(

			( V, Hook = () => {} ) => Hook( V )

		)

		// Export Promise
		return Princess

	}

}
